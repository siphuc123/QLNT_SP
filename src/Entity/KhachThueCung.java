package Entity;

public class KhachThueCung {
    private String MaKTC;
    private String HoTen;
    private boolean GioiTinh;
    private String SoDT;
    private String MaKTDD;
    private String HoTen_DD;

    public String getMaKTC() {
        return MaKTC;
    }

    public void setMaKTC(String MaKTC) {
        this.MaKTC = MaKTC;
    }

    public String getHoTen() {
        return HoTen;
    }

    public void setHoTen(String HoTen) {
        this.HoTen = HoTen;
    }

    public boolean isGioiTinh() {
        return GioiTinh;
    }

    public void setGioiTinh(boolean GioiTinh) {
        this.GioiTinh = GioiTinh;
    }

    public String getSoDT() {
        return SoDT;
    }

    public void setSoDT(String SoDT) {
        this.SoDT = SoDT;
    }

    public String getMaKTDD() {
        return MaKTDD;
    }

    public void setMaKTDD(String MaKTDD) {
        this.MaKTDD = MaKTDD;
    }

    public String getHoTen_DD() {
        return HoTen_DD;
    }

    public void setHoTen_DD(String HoTen_DD) {
        this.HoTen_DD = HoTen_DD;
    }
    
    
}
